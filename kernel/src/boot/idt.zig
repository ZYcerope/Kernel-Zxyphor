// =============================================================================
// User-installable IRQ handlers
// =============================================================================
const IrqHandler = *const fn (u8) void;
var irq_handlers: [16]?IrqHandler = [_]?IrqHandler{null} ** 16;

/// Register a handler for a specific IRQ (0-15)
pub fn registerIrqHandler(irq: u8, handler: IrqHandler) void {
    if (irq < 16) {
        irq_handlers[irq] = handler;
        main.klog(.debug, "IDT: Registered handler for IRQ{d}", .{irq});
    }
}

/// Unregister a handler for a specific IRQ
pub fn unregisterIrqHandler(irq: u8) void {
    if (irq < 16) {
        irq_handlers[irq] = null;
    }
}

// =============================================================================
// Main interrupt/exception handler — called from assembly common stub
// =============================================================================
export fn isrHandler(frame: *InterruptFrame) void {
    const vector = frame.vector;

    if (vector < 32) {
        // CPU exception
        handleException(frame);
    } else if (vector >= 32 and vector < 48) {
        // Hardware IRQ (PIC IRQ 0-15)
        const irq: u8 = @truncate(vector - 32);
        handleIrq(irq, frame);
    } else if (vector == 0x80) {
        // Legacy system call (int 0x80)
        main.syscall_handler.handleLegacySyscall(frame);
    }
}

/// Handle a CPU exception
fn handleException(frame: *InterruptFrame) void {
    const vector: usize = @truncate(frame.vector);
    const name = if (vector < 32) exception_names[vector] else "Unknown";

    // Double fault and machine check are always fatal
    if (vector == 8 or vector == 18) {
        main.serial.writer().print("\r\nFATAL: {s} (vector {d})\r\n", .{ name, vector }) catch {};
        main.serial.writer().print("Error code: 0x{x}\r\n", .{frame.error_code}) catch {};
        main.serial.writer().print("RIP: 0x{x:0>16}\r\n", .{frame.rip}) catch {};
        main.serial.writer().print("RSP: 0x{x:0>16}\r\n", .{frame.rsp}) catch {};
        main.serial.writer().print("CR2: 0x{x:0>16}\r\n", .{main.registers.readCr2()}) catch {};
        @panic(name);
    }

    // Page fault needs special handling
    if (vector == 14) {
        handlePageFault(frame);
        return;
    }

    // General protection fault
    if (vector == 13) {
        main.serial.writer().print("\r\nGeneral Protection Fault!\r\n", .{}) catch {};
        main.serial.writer().print("Error code: 0x{x}\r\n", .{frame.error_code}) catch {};
        main.serial.writer().print("RIP: 0x{x:0>16}\r\n", .{frame.rip}) catch {};
        main.serial.writer().print("CS:  0x{x}\r\n", .{frame.cs}) catch {};

        // If it was in user mode, kill the process; otherwise panic
        if (frame.cs & 3 != 0) {
            main.klog(.err, "GPF in userspace at RIP=0x{x}, killing process", .{frame.rip});
            main.process.killCurrent(.segfault);
        } else {
            @panic("General Protection Fault in kernel mode");
        }
        return;
    }

    // Other exceptions — log and potentially kill the process
    main.klog(.err, "Exception: {s} (#{d}) at RIP=0x{x}", .{ name, vector, frame.rip });

    if (frame.cs & 3 != 0) {
        // Exception in user mode — kill the process
        main.process.killCurrent(.exception);
    } else {
        // Exception in kernel mode — this is bad
        @panic("Unhandled exception in kernel mode");
    }
}

/// Handle a page fault (vector 14)
fn handlePageFault(frame: *InterruptFrame) void {
    const fault_addr = main.registers.readCr2();
    const error_code = frame.error_code;

    // Decode page fault error code bits
    const present = (error_code & 1) != 0;
    const write = (error_code & 2) != 0;
    const user = (error_code & 4) != 0;
    const reserved_write = (error_code & 8) != 0;
    const instruction_fetch = (error_code & 16) != 0;

    main.klog(.debug, "Page fault at 0x{x:0>16}: present={} write={} user={} rsvd={} ifetch={}", .{
        fault_addr,
        present,
        write,
        user,
        reserved_write,
        instruction_fetch,
    });

    // Try to handle the page fault through the VMM (demand paging, COW, etc.)
    if (main.vmm.handlePageFault(fault_addr, error_code)) {
        return; // Handled successfully — page has been mapped
    }

    // Unresolvable page fault
    if (user) {
        main.klog(.err, "Segmentation fault: process accessed 0x{x}", .{fault_addr});
        main.process.killCurrent(.segfault);
    } else {
        main.serial.writer().print("\r\nKernel Page Fault!\r\n", .{}) catch {};
        main.serial.writer().print("Fault address: 0x{x:0>16}\r\n", .{fault_addr}) catch {};
        main.serial.writer().print("RIP: 0x{x:0>16}\r\n", .{frame.rip}) catch {};
        main.serial.writer().print("Error: P={} W={} U={}\r\n", .{ present, write, user }) catch {};
        @panic("Unresolvable page fault in kernel");
    }
}

/// Handle a hardware IRQ from the PIC
fn handleIrq(irq: u8, frame: *InterruptFrame) void {
    _ = frame;

    // Dispatch to registered handler
    if (irq < 16) {
        if (irq_handlers[irq]) |handler| {
            handler(irq);
        }
    }

    // Send End-of-Interrupt to PIC
    main.pic.sendEoi(irq);
}

// =============================================================================
// Initialize the IDT
// =============================================================================
pub fn initialize() void {
    // Install exception handlers (vectors 0-31)
    for (0..32) |i| {
        const handler_addr = @intFromPtr(isr_stubs[i]);
        const ist_index: u3 = if (i == 8) 1 else if (i == 2) 2 else 0;
        idt_entries[i] = IdtEntry.create(
            handler_addr,
            gdt.KERNEL_CODE_SELECTOR,
            ist_index,
            GATE_INTERRUPT,
            DPL_KERNEL,
        );
    }

    // Install IRQ handlers (vectors 32-47)
    for (0..16) |i| {
        const handler_addr = @intFromPtr(irq_stubs[i]);
        idt_entries[32 + i] = IdtEntry.create(
            handler_addr,
            gdt.KERNEL_CODE_SELECTOR,
            0,
            GATE_INTERRUPT,
            DPL_KERNEL,
        );
    }

    // Install syscall interrupt handler (vector 0x80) — accessible from ring 3
    idt_entries[0x80] = IdtEntry.create(
        @intFromPtr(makeIsrStub(0x80, false)),
        gdt.KERNEL_CODE_SELECTOR,
        0,
        GATE_TRAP, // Trap gate so interrupts stay enabled during syscall
        DPL_USER, // Callable from user mode
    );

    // Load the IDT
    idt_pointer = IdtPointer{
        .limit = @as(u16, @sizeOf(@TypeOf(idt_entries))) - 1,
        .base = @intFromPtr(&idt_entries),
    };

    asm volatile ("lidt (%[idt_ptr])"
        :
        : [idt_ptr] "r" (&idt_pointer),
        : "memory"
    );

    main.klog(.info, "IDT: Loaded with {d} entries", .{IDT_ENTRIES});
}

/// Set an IDT entry directly (for advanced use cases like APIC interrupts)
pub fn setEntry(vector: u8, handler_addr: u64, ist_index: u3, gate_type: u4, dpl: u2) void {
    idt_entries[vector] = IdtEntry.create(
        handler_addr,
        gdt.KERNEL_CODE_SELECTOR,
        ist_index,
        gate_type,
        dpl,
    );
}
