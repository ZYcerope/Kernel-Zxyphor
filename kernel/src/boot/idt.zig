// =============================================================================
// Kernel Zxyphor - Interrupt Descriptor Table (IDT)
// =============================================================================
// The IDT maps interrupt/exception vectors (0-255) to handler functions.
// x86_64 uses 64-bit interrupt gate descriptors. Each entry specifies:
//   - The handler function address (split across multiple fields)
//   - The code segment selector to use
//   - Stack table index (for IST — Interrupt Stack Table)
//   - Gate type and privilege level
//
// Vectors 0-31:  CPU exceptions (divide error, page fault, etc.)
// Vectors 32-47: IRQs (remapped from PIC default 0-15)
// Vector 128:    System call interrupt (0x80, legacy — we also use SYSCALL)
// =============================================================================

const main = @import("../main.zig");
const gdt = @import("gdt.zig");

// =============================================================================
// IDT constants
// =============================================================================
pub const IDT_ENTRIES: usize = 256;

// Gate types for 64-bit mode
const GATE_INTERRUPT: u4 = 0xE; // Interrupt gate — clears IF (disables interrupts)
const GATE_TRAP: u4 = 0xF; // Trap gate — does NOT clear IF

// DPL (Descriptor Privilege Level)
const DPL_KERNEL: u2 = 0;
const DPL_USER: u2 = 3;

// =============================================================================
// IDT Entry (16 bytes in 64-bit mode)
// =============================================================================
const IdtEntry = packed struct {
    offset_low: u16, // Handler address bits 0-15
    selector: u16, // Code segment selector (usually 0x08)
    ist: u3, // Interrupt Stack Table index (0 = don't switch)
    reserved0: u5, // Must be zero
    gate_type: u4, // Gate type (interrupt or trap)
    zero: u1, // Must be zero
    dpl: u2, // Minimum privilege level for software interrupt
    present: u1, // Must be 1 for valid entries
    offset_mid: u16, // Handler address bits 16-31
    offset_high: u32, // Handler address bits 32-63
    reserved1: u32, // Must be zero

    /// Create an IDT entry for a handler function
    pub fn create(handler: u64, selector: u16, ist_index: u3, gate_type: u4, dpl: u2) IdtEntry {
        return IdtEntry{
            .offset_low = @truncate(handler),
            .selector = selector,
            .ist = ist_index,
            .reserved0 = 0,
            .gate_type = gate_type,
            .zero = 0,
            .dpl = dpl,
            .present = 1,
            .offset_mid = @truncate(handler >> 16),
            .offset_high = @truncate(handler >> 32),
            .reserved1 = 0,
        };
    }

    /// Create a not-present (empty) IDT entry
    pub fn empty() IdtEntry {
        return IdtEntry{
            .offset_low = 0,
            .selector = 0,
            .ist = 0,
            .reserved0 = 0,
            .gate_type = 0,
            .zero = 0,
            .dpl = 0,
            .present = 0,
            .offset_mid = 0,
            .offset_high = 0,
            .reserved1 = 0,
        };
    }
};

// =============================================================================
// IDT Pointer (IDTR) — loaded with lidt instruction
// =============================================================================
const IdtPointer = packed struct {
    limit: u16,
    base: u64,
};

// =============================================================================
// IDT table and pointer
// =============================================================================
var idt_entries: [IDT_ENTRIES]IdtEntry align(16) = [_]IdtEntry{IdtEntry.empty()} ** IDT_ENTRIES;
var idt_pointer: IdtPointer = undefined;

// =============================================================================
// ISR stub type — generated by assembly macro
// =============================================================================
const IsrStub = *const fn () callconv(.Naked) void;

// =============================================================================
// Exception handler stubs (vectors 0-31)
// Each stub pushes an error code (or dummy), the vector number, and jumps
// to the common handler. Exceptions that push error codes: 8,10,11,12,13,14,17,21,29,30
// =============================================================================
fn makeIsrStub(comptime vector: u8, comptime has_error_code: bool) IsrStub {
    return struct {
        fn handler() callconv(.Naked) void {
            if (!has_error_code) {
                // Push a dummy error code so the stack frame is uniform
                asm volatile ("pushq $0");
            }
            // Push the interrupt vector number
            asm volatile ("pushq %[vec]"
                :
                : [vec] "i" (@as(u64, vector)),
            );
            // Jump to common handler
            asm volatile ("jmp isr_common_stub");
        }
    }.handler;
}

// CPU exception stubs (vectors 0-31)
const isr_stubs = blk: {
    var stubs: [32]IsrStub = undefined;
    // Vectors with error codes: 8, 10, 11, 12, 13, 14, 17, 21, 29, 30
    const error_code_vectors = [_]u8{ 8, 10, 11, 12, 13, 14, 17, 21, 29, 30 };

    for (0..32) |i| {
        var has_error = false;
        for (error_code_vectors) |ec| {
            if (i == ec) {
                has_error = true;
                break;
            }
        }
        stubs[i] = makeIsrStub(@intCast(i), has_error);
    }
    break :blk stubs;
};

// IRQ handler stubs (vectors 32-47)
fn makeIrqStub(comptime irq: u8) IsrStub {
    return makeIsrStub(32 + irq, false);
}

const irq_stubs = blk: {
    var stubs: [16]IsrStub = undefined;
    for (0..16) |i| {
        stubs[i] = makeIrqStub(@intCast(i));
    }
    break :blk stubs;
};

// =============================================================================
// Common ISR handler (called from assembly stubs)
// =============================================================================
// Stack frame when we arrive here:
//   [rsp+0]   : vector number
//   [rsp+8]   : error code (real or dummy 0)
//   [rsp+16]  : RIP (return address pushed by CPU)
//   [rsp+24]  : CS
//   [rsp+32]  : RFLAGS
//   [rsp+40]  : RSP (if privilege change)
//   [rsp+48]  : SS (if privilege change)
// =============================================================================
const InterruptFrame = extern struct {
    // Registers pushed by our common stub
    r15: u64,
    r14: u64,
    r13: u64,
    r12: u64,
    r11: u64,
    r10: u64,
    r9: u64,
    r8: u64,
    rbp: u64,
    rdi: u64,
    rsi: u64,
    rdx: u64,
    rcx: u64,
    rbx: u64,
    rax: u64,
    // Pushed by ISR stub
    vector: u64,
    error_code: u64,
    // Pushed by CPU on interrupt
    rip: u64,
    cs: u64,
    rflags: u64,
    rsp: u64,
    ss: u64,
};

/// Common assembly stub that saves registers and calls the Zig handler
export fn isr_common_stub() callconv(.Naked) void {
    // Save all general-purpose registers
    asm volatile (
        \\ pushq %%rax
        \\ pushq %%rbx
        \\ pushq %%rcx
        \\ pushq %%rdx
        \\ pushq %%rsi
        \\ pushq %%rdi
        \\ pushq %%rbp
        \\ pushq %%r8
        \\ pushq %%r9
        \\ pushq %%r10
        \\ pushq %%r11
        \\ pushq %%r12
        \\ pushq %%r13
        \\ pushq %%r14
        \\ pushq %%r15
        \\
        \\ movq %%rsp, %%rdi   // Pass frame pointer as first argument
        \\ call isrHandler      // Call the Zig handler
        \\
        \\ popq %%r15
        \\ popq %%r14
        \\ popq %%r13
        \\ popq %%r12
        \\ popq %%r11
        \\ popq %%r10
        \\ popq %%r9
        \\ popq %%r8
        \\ popq %%rbp
        \\ popq %%rdi
        \\ popq %%rsi
        \\ popq %%rdx
        \\ popq %%rcx
        \\ popq %%rbx
        \\ popq %%rax
        \\
        \\ addq $16, %%rsp     // Remove error code and vector number
        \\ iretq               // Return from interrupt
    );
}

